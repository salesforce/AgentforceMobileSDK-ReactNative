/**
 * Generates AppConfig.generated.ts with the correct APP_MODE for the build target.
 *
 * Usage: node scripts/generate-app-config.js [service|employee|all]
 */

const fs = require('fs');
const path = require('path');

const target = process.argv[2] || 'all';

if (!['service', 'employee', 'all'].includes(target)) {
  console.error('❌ Invalid target: ' + target);
  console.error('   Usage: node scripts/generate-app-config.js [service|employee|all]');
  process.exit(1);
}

const configContent = `/**
 * Generated App Configuration
 *
 * ⚠️ This file is auto-generated by scripts/generate-app-config.js
 * Do not edit manually - changes will be overwritten on next build
 *
 * To change the app mode, run:
 * node scripts/generate-app-config.js [service|employee|all]
 */

export const APP_MODE = '${target}' as const;
`;

const outputPath = path.join(__dirname, '..', 'src', 'config', 'AppConfig.generated.ts');
fs.writeFileSync(outputPath, configContent, 'utf8');

console.log('✅ Generated AppConfig.generated.ts with APP_MODE=' + target);
